const input = rawInput.split("\n");
const rowLength = input[0].length;
const tree = "#";

const slopes = [
  [1, 1],
  [3, 1],
  [5, 1],
  [7, 1],
  [1, 2]
];

const slopeResults = slopes.map(
  (slope) =>
    input.reduce(
      (acc, row, index) => {
        const stepDown = slope[1];
        if (index % stepDown !== 0) return acc;

        const stepRight = slope[0];
        const treeCount = Array.from(row)[acc[0]] === tree ? 1 : 0;

        return [(acc[0] + stepRight) % rowLength, acc[1] + treeCount];
      },
      [0, 0]
    )[1]
);

const answer = slopeResults.reduce((acc, result) => acc * result, 1);

document.getElementById("app").innerHTML = `${answer}; ${slopeResults}`;
